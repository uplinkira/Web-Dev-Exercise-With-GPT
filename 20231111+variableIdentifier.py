# 定义一个变量并赋值 Define a variable and assign a value to it.
my_variable = 42
# 在上面的示例中，my_variable 是一个标识符，它用于存储整数值，并且可以在程序中多次使用来引用相同的值或不同的值。这演示了标识符是用来命名变量的，而变量则用来存储数据。
# In the above example, my_variable is an identifier used to store an integer value
# and it can be used multiple times in the program to reference the same or different values. 
# This demonstrates that identifiers are used to name variables
# and variables are used to store data.

# 使用变量 Using the variable.
print(my_variable)  # 输出：42

# 修改变量的值 Modifying the variable's value.
my_variable = 100

# 再次使用变量 Using the variable again.
print(my_variable)  # 输出：100
